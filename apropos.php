                               <div class="col-md-12">
                                 L’initiative de la création du Centre Polytechnique du Centre (CPC) est née d’un constat.</p>
                                 La région des lacs aujourd’hui le bélier, accusait un déficit criard en matière de structures de formation professionnelle et d’enseignement technique.</p>
                                Le vide dans ce domaine est apparu si préoccupant que Monsieur YAO Kouassi Maurice a décidé de le combler. Dès 1993, il créé le CPC secondaire technique qui fut autorisé et reconnu par les décisions n°2199/MEN/CAB/SEP du 03 octobre 1994 et 97/170/METFP/DETP/S-DEP du 18 septembre 1997.</p>
                                La situation de manque d’écoles privées d’enseignement supérieur dans la région des lacs lui est apparue d’autant plus préoccupante que Monsieur YAO Kouassi Maurice créa au CPC, les cycles du BTS et de l’ingénierie tertiaire et industriel agréés respectivement par les décisions n°299/MESRIT/DESUP/S-DEP du 12 novembre 1996 et 180/MESRS/DESPRIV/S-DAH/CF du 23 mai 2012</p>
                            Mieux, un cabinet de formation initiale et continue (CAFOPLA) est créé et son habilitation est renouvelée chaque année par le FDFP. Par ailleurs, le CPC dispose d’un cabinet de placement agréé par le ministère de l’emploi et de la fonction publique depuis le 1er avril 1993 sous le n°03846/EFP/CAB-1. Vingt-huit ans après la création du CPC, que pouvons-nous dire de l’initiative de Monsieur YAO Kouassi Maurice ? Qui l’eût cru, serait-on tenté de dire. L’action de Monsieur Maurice a porté. Aujourd’hui, toute la région du bélier regorge de nombreuses écoles privées d’enseignement technique secondaire et supérieur et de cabinets de formation.</p>
                                 Le CAFOPLA vise l’insertion des apprenants dans la vie active et au renforcement des capacités des salariés.</p>
                                 
