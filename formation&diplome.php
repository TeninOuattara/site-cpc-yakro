function check_annee()
{
    $('#BtnEnregistrer').attr('value', "modifier");
    var annee = $("#annee").val().trim();

    //Ajax Load data from ajax
    $.ajax({
    url : "https://scolarite.uvci.edu.ci/Main/get_annee/" + annee,
    type: "POST",
    dataType: "JSON",
    success: function(data)
    {
        if(data == '1')
        {
            $(".libelle_error").html("");
            $(".submit").attr('disabled', false);
        }
        else
        {
            $(".libelle_error").html(" <b>Veuillez sélectionner l'année du BAC "+data.annee+".</b>");
            $(".submit").attr('disabled', true);

            event.preventDefault();
        }

    },
    error: function (jqXHR, textStatus, errorThrown)
    {
        alert('Error get data from ajax');
    }
    });
}

